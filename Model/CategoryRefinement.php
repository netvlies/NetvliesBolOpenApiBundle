<?php

namespace Netvlies\Bundle\BolOpenApi\Bundle;

// @todo misses in XML?
class CategoryRefinement
{
    protected $id;
    protected $name;

    /**
     * @param $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}